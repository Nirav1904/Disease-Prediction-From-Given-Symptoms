from pandas import read_pickle
from numpy import array
from abc import ABC,abstractmethod

class DiseaseClassifier(ABC):
    knn_clf=read_pickle("knn_model.pkl")
    dt_clf=read_pickle("decisionTree_model.pkl")

    @abstractmethod
    def predictDisease(self,symptoms):
        pass

class DecisionTreeClassifier(DiseaseClassifier):

    def predictDisease(self, symptoms):
        return super().dt_clf.predict(array([symptoms]))
        

class KNNClassifier(DiseaseClassifier):

    def predictDisease(self, symptoms):
        return super().knn_clf.predict(array([symptoms]))


